#include<iostream.h>
#include<conio.h>

class RING
{
	int RingNumber;
	float Radius,Area;
	void CalcArea()
	{
		Area=3.14*Radius*Radius;
	}
	public:
	void GetArea()
	{
		cout<<"\n\tENTER THE VALUE OF RING NUMBER:";
		cin>>RingNumber;
		cout<<"\n\tENTER THE RADIUS:";
		cin>>Radius;
		CalcArea();
	}
	void ShowArea()
	{
		cout<<"\n\tTHE VALUE OF RING NUMBER IS:"<<RingNumber;
		cout<<"\n\tTHE VALUE OF RADIUS IS:"<<Radius;
		cout<<"\n\tTHE VALUE OF AREA IS:"<<Area;
	}
};
void main()
{
	clrscr();
	RING R1;
	R1.GetArea();
	R1.ShowArea();
	getch();
}





